class Solution:
    def evalRPN(self, tokens: List[str]) -> int:
        stack = []
        for c in tokens:
            if c == '+':
                stack.append(stack.pop() + stack.pop())
            elif c == '-':
                b= stack.pop()
                a = stack.pop()
                stack.append(a-b)
            elif c == '*':
                stack.append(stack.pop() * stack.pop())
            elif c == '/':
                b = stack.pop()
                a= stack.pop()
                stack.append(int(a/b)) # truncates towards 0
            else:
                stack.append(int(c))
            
        return stack[0]

